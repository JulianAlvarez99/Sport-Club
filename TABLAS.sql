CREATE TABLE CRONOGRAMA (
    COD_CRONOGRAMA INT PRIMARY KEY NOT NULL,
    PERIODO DATE,
    LEGAJO INT,
    COD_ACTIVIDAD INT,
    COD_AREA INT,
    COD_TURNO INT,
